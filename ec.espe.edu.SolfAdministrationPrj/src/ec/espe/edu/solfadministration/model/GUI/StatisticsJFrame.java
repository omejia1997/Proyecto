    private void seeReport(ArrayList<String> saleData) {

        String matriz[][] = new String[saleData.size()][4];
        for (int i = 0; i < saleData.size(); i++) {
            matriz[i][0] = saleData.get(i).split("-")[0];
            matriz[i][1] = saleData.get(i).split("-")[1];

            String aux = saleData.get(i).split("-")[2];
            matriz[i][2] = aux.split("|")[0] + aux.split("|")[1];

            System.out.println(matriz[i][2]);

            matriz[i][3] = Character.toString(aux.charAt(6)) + Character.toString(aux.charAt(7)) + Character.toString(aux.charAt(8)) + "  ";
            System.out.println(matriz[i][3]);

        }
        DefaultTableModel modelTable = new DefaultTableModel(matriz, new String[]{"Año", "Mes", "Día", "Ventas"}) {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        };
        lblTotal.setText("TOTAL: " + statistic.getValueIncome());
        tblReport.setModel(modelTable);

    }

