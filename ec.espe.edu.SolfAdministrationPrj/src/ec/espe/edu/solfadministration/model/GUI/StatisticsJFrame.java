    private void btnQueryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQueryActionPerformed
        int queryYear = Integer.parseInt(String.valueOf(cmbYear.getSelectedItem()));

        int queryDay = Integer.parseInt(String.valueOf(cmbDay.getSelectedItem()));

        int queryMonth = Integer.parseInt(String.valueOf(cmbMonth.getSelectedItem()));

        Date queryDate = new Date(queryYear, queryMonth, queryDay);
        System.out.println(queryDate.getDate());
        queryDate = Validations.ValidateBirthDate(queryDate);
        if (queryDate.getDate().equals("-1/-1/-1")) {
            lblErrorDate.setText("Fecha incorrecta");
        } else {
            lblErrorDate.setText("");
        }

        querySales = statistic.viewStatisticsBySpecificDate(queryYear, queryMonth, queryDay);

        seeReport(querySales);


    }//GEN-LAST:event_btnQueryActionPerformed

